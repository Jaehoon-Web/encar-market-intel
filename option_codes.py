def decode_options(standard_codes: list, etc_codes: list = None, choice_codes: list = None) -> str:
    """옵션 코드 리스트 → 한국어 이름 리스트 변환"""
    names = []
    for code in (standard_codes or []):
        names.append(STANDARD_OPTIONS.get(str(code), f"옵션_{code}"))
    for code in (etc_codes or []):
        names.append(f"기타_{code}")
    for code in (choice_codes or []):
        names.append(CHOICE_OPTIONS.get(str(code), f"선택_{code}"))
    return ", ".join(names)
